package day07loops;

public class C07WhileLoops {
    public static void main(String[] args) {

        // example: type code to print integers from 4 to 7
            //1.yol
        for(int i=4; i<8; i++){
            System.out.print(i+ " ");

        }
        System.out.println();
        //2.yol
        int i=4;
        while(i<8){
            System.out.print(i+ " ");
            i++;
        }
    }
}
